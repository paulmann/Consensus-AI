    /**
     * System prompt for arbitrary Skill creation from user query.
     */
    private function buildSystemPromptForArbitrarySkill(): string
    {
        return 'You are an expert system designer for a multi-agent AI council engine. ' .
            'Generate a JSON object that defines a SKILL for handling the user\'s query. ' .
            'Output MUST be valid JSON only, with no markdown or surrounding text. ' .
            'The JSON must contain at least: name (snake_case), description, roles (1-10), ' .
            'consensus ("majority_vote"|"weighted_average"|"synthesis"), and tags (array of strings). ' .
            'Each role should have: id, name, purpose, system_prompt, and may have style, constraints, and weight.';
    }

    /**
     * User prompt for arbitrary Skill creation from user query.
     */
    private function buildUserPromptForArbitrarySkill(string $userQuery): string
    {
        return 'User query: ' . $userQuery . '\n' .
            'Define a SKILL that is best suited to address this query. ' .
            'Make the name short and descriptive in snake_case, and choose roles that naturally decompose the problem.';
    }
