declare(strict_types=1);

namespace App\Application\Council;

use App\Domain\Council\Skill;
use App\Domain\Council\SkillRepositoryInterface;
use App\Infrastructure\BothubChat\BothubClient;
use DateTimeImmutable;
use JsonException;
use Psr\Log\LoggerInterface;
use RuntimeException;
use Throwable;

/**
 * Senior-level implementation of automatic Skill generation using LLM.
 */
readonly final class SkillAutoCreator
{
    private const MODEL = 'gpt-4o';

    public function __construct(
        private BothubClient $bothubClient,
        private SkillRepositoryInterface $skillRepository,
        private SkillSearch $skillSearch,
        private LoggerInterface $logger
    ) {}

    /**
     * Create a new SKILL from a user query using LLM.
     *
     * On failure, falls back to seeding the default consensus SKILL.
     */
    public function createFromQuery(string $userQuery, ?int $sessionId = null): Skill
    {
        $userQuery = trim($userQuery);

        $this->logger->info('SkillAutoCreator: creating Skill from query', [
            'session_id'    => $sessionId,
            'query_preview' => mb_substr($userQuery, 0, 160),
        ]);

        if ($userQuery === '') {
            $this->logger->warning('SkillAutoCreator: empty user query, using default consensus SKILL', [
                'session_id' => $sessionId,
            ]);

            return $this->seedDefaultConsensusSkill();
        }

        try {
            $systemPrompt = $this->buildSystemPromptForArbitrarySkill();
            $userPrompt   = $this->buildUserPromptForArbitrarySkill($userQuery);

            $response = $this->bothubClient->createChatCompletion(
                model: self::MODEL,
                messages: [
                    ['role' => 'system', 'content' => $systemPrompt],
                    ['role' => 'user',   'content' => $userPrompt],
                ],
                options: [
                    'temperature' => 0.4,
                    'max_tokens'  => 2000,
                ]
            );

            $content = $response['choices'][0]['message']['content'] ?? null;
            if (!is_string($content) || trim($content) === '') {
                throw new RuntimeException('LLM response has no content');
            }

            try {
                /** @var array<string,mixed> $data */
                $data = json_decode($content, true, 512, JSON_THROW_ON_ERROR);
            } catch (JsonException $e) {
                throw new RuntimeException('LLM response is not valid JSON: ' . $e->getMessage(), 0, $e);
            }

            $skill = $this->createSkillFromArray($data, isAutoGenerated: true);

            $this->logger->info('SkillAutoCreator: Skill created from query via LLM', [
                'session_id' => $sessionId,
                'skill_id'   => $skill->getId(),
                'name'       => $skill->getName(),
            ]);

            return $skill;
        } catch (Throwable $e) {
            $this->logger->error('SkillAutoCreator: createFromQuery failed, falling back to default SKILL', [
                'session_id' => $sessionId,
                'error'      => $e->getMessage(),
            ]);

            return $this->seedDefaultConsensusSkill();
        }
    }

    /**
     * Seed a default, general-purpose consensus SKILL.
     *
     * Tries LLM-first, then falls back to a canonical built-in JSON definition.
     */
    public function seedDefaultConsensusSkill(): Skill
    {
        try {
            $skill = $this->generateDefaultSkillViaLlm();
            $this->logger->info('SkillAutoCreator: default consensus SKILL generated via LLM', [
                'skill_name' => $skill->getName(),
            ]);

            return $skill;
        } catch (Throwable $e) {
            $this->logger->warning('SkillAutoCreator: LLM generation failed, falling back to built-in default SKILL', [
                'error' => $e->getMessage(),
            ]);
        }

        $fallbackJson = $this->getDefaultSkillJson();

        try {
            /** @var array<string,mixed> $data */
            $data = json_decode($fallbackJson, true, 512, JSON_THROW_ON_ERROR);
        } catch (JsonException $e) {
            $this->logger->error('SkillAutoCreator: fallback JSON for default SKILL is invalid', [
                'error' => $e->getMessage(),
            ]);
            throw $e;
        }

        return $this->createSkillFromArray($data, isAutoGenerated: false);
    }

    /**
     * Generate the default consensus SKILL via LLM using an internal spec.
     */
    private function generateDefaultSkillViaLlm(): Skill
    {
        $systemPrompt = 'You are a system designer tasked with defining a general-purpose ' .
            'deliberative SKILL for a multi-agent council. You MUST output a SINGLE JSON object ' .
            'with the following structure: ' .
            '{"name": string (snake_case), "description": string, "roles": [role...], ' .
            '"consensus": {"strategy": "majority_vote"|"weighted_average"|"synthesis", "parameters": object}, ' .
            '"tags": [string...], "meta": object}. ' .
            'Each role must have: id, name, purpose, style, constraints, system_prompt, weight. ' .
            'Do NOT include any text before or after the JSON. Do NOT wrap JSON in code fences.';

        $userPrompt = 'Define a default, general-purpose "general_consensus_council" SKILL that: ' .
            '1) analyzes complex user queries from multiple expert perspectives, ' .
            '2) critiques its own reasoning, ' .
            '3) produces a synthesized, well-argued final answer with explicit trade-offs and uncertainties. ' .
            'Use exactly 4 roles with the following identities: ' .
            '(1) Analytical Expert, (2) Critical Reviewer, (3) User Advocate, (4) Synthesis Arbiter. ' .
            'Make sure the "name" field is "general_consensus_council" in snake_case.';

        $response = $this->bothubClient->createChatCompletion(
            model: self::MODEL,
            messages: [
                ['role' => 'system', 'content' => $systemPrompt],
                ['role' => 'user',   'content' => $userPrompt],
            ],
            options: [
                'temperature' => 0.3,
                'max_tokens'  => 2000,
            ]
        );

        $content = $response['choices'][0]['message']['content'] ?? null;
        if (!is_string($content) || trim($content) === '') {
            throw new RuntimeException('LLM response has no content');
        }

        try {
            /** @var array<string,mixed> $data */
            $data = json_decode($content, true, 512, JSON_THROW_ON_ERROR);
        } catch (JsonException $e) {
            throw new RuntimeException('LLM response is not valid JSON: ' . $e->getMessage(), 0, $e);
        }

        return $this->createSkillFromArray($data, isAutoGenerated: true);
    }

    /**
     * Build a Skill entity from decoded JSON and persist + index it.
     *
     * @param array<string,mixed> $data
     */
    private function createSkillFromArray(array $data, bool $isAutoGenerated): Skill
    {
        $name        = (string) ($data['name'] ?? '');
        $description = (string) ($data['description'] ?? '');
        $roles       = (array)  ($data['roles'] ?? []);
        $tags        = (array)  ($data['tags'] ?? []);

        $consensusRaw = $data['consensus']['strategy'] ?? ($data['consensus'] ?? 'synthesis');
        $consensus    = is_string($consensusRaw) ? $consensusRaw : 'synthesis';

        if ($name === '' || $description === '') {
            throw new RuntimeException('SKILL JSON must contain non-empty name and description');
        }

        $skill = new Skill(
            id: 0,
            name: $name,
            description: $description,
            roles: $roles,
            consensus: $consensus,
            tags: $tags,
            createdAt: new DateTimeImmutable('now'),
            updatedAt: null,
        );

        $saved = $this->skillRepository->save($skill, $isAutoGenerated);
        $this->skillSearch->indexSkill($saved);

        return $saved;
    }

